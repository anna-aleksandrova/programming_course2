#include <stdio.h>

int main(){
    int x1 = 2 + 31;
    printf("2 + 31 = %d\n", x1);
    int x2 = 45 * 54 - 11;
    printf("45 * 54 - 11 = %d\n", x2);
    int x3 = 15 / 4;
    printf("15 / 4 = %d\n", x3);
    float x4 = 15.0 / 4;
    printf("15.0 / 4 = %.3f\n", x4);
    int x5 = 67 % 5;
    printf("67 %% 5 = %d\n", x5);
    float x6 = (2 * 45.1 + 3.2) / 2;
    printf("(2 * 45.1 + 3.2) / 2 = %.2f\n", x6);
}